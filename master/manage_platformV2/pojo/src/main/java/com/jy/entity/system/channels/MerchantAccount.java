package com.jy.entity.system.channels;

/**
 * 商户返佣账户信息
 * Created by Administrator on 2017/1/6.
 */
public class MerchantAccount {
    private Integer id;
    private Integer merchantId;//渠道商户主键
    private String account;//账户号
    private String accountType;//账户类型，1.银行账户，2，微信，3，支付宝
    private Integer isEnable;//是否启用，1，启用，2，禁用
    private String userName;//
    private String accountName;//账户名
    private String bankName;//银行名称
    private String subBankName;//支行名

    public void setMerchantId(Integer merchantId) {
        this.merchantId = merchantId;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public Integer getIsEnable() {
        return isEnable;
    }

    public Integer getMerchantId() {
        return merchantId;
    }

    public String getAccount() {
        return account;
    }

    public String getAccountName() {
        return accountName;
    }

    public String getAccountType() {
        return accountType;
    }

    public String getBankName() {
        return bankName;
    }

    public String getSubBankName() {
        return subBankName;
    }

    public String getUserName() {
        return userName;
    }

    public void setAccountName(String accountName) {
        this.accountName = accountName;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public void setAccountType(String accountType) {
        this.accountType = accountType;
    }

    public void setBankName(String bankName) {
        this.bankName = bankName;
    }

    public void setIsEnable(Integer isEnable) {
        this.isEnable = isEnable;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setSubBankName(String subBankName) {
        this.subBankName = subBankName;
    }
}
