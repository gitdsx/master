public class TaskWithdrawInfo {
    private String batchNo;//TID
    private String transTime;//
    private Integer userId;//提现用户编号
    private Double tMoney;//提现金额
    private String tAccount;//体现账户
    private String tBank;//提现银行
    private int tStatus;//提现状态 1=成功 2=失败 3=处理中 4=其它
    private String tResultDate;//结果返回时间
    private String tFlowNo;//交易流水号
    private String tUserName;

    public String getBatchNo() {
        return batchNo;
    }

    public void setBatchNo(String batchNo) {
        this.batchNo = batchNo;
    }

    public String getTransTime() {
        return transTime;
    }

    public void setTransTime(String transTime) {
        this.transTime = transTime;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Double gettMoney() {
        return tMoney;
    }

    public void settMoney(Double tMoney) {
        this.tMoney = tMoney;
    }

    public String gettAccount() {
        return tAccount;
    }

    public void settAccount(String tAccount) {
        this.tAccount = tAccount;
    }

    public String gettBank() {
        return tBank;
    }

    public void settBank(String tBank) {
        this.tBank = tBank;
    }

    public int gettStatus() {
        return tStatus;
    }

    public void settStatus(int tStatus) {
        this.tStatus = tStatus;
    }

    public String gettResultDate() {
        return tResultDate;
    }

    public void settResultDate(String tResultDate) {
        this.tResultDate = tResultDate;
    }

    public String gettFlowNo() {
        return tFlowNo;
    }

    public void settFlowNo(String tFlowNo) {
        this.tFlowNo = tFlowNo;
    }

    public String gettUserName() {
        return tUserName;
    }

    public void settUserName(String tUserName) {
        this.tUserName = tUserName;
    }
}
