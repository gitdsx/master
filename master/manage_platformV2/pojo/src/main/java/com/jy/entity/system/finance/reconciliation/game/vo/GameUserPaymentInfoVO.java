public class GameUserPaymentInfoVO implements Serializable {
	
	private static final long serialVersionUID = 6907090126948686969L;
	
	@SerializedName("PayId")
	private String payId; // 交易记录号
	
	@SerializedName("UserId")
	private String userId; // 用户编号
	
	@SerializedName("PayFlowNo")
	private String payFlowNo; // 第三方流水
	
	@SerializedName("TransMoney")
	private Integer transMoney; // 交易金额以分为单位
	
	@SerializedName("PayWay")
	private Integer payWay; // 第三方支付渠道1=微信，2=银联，3=得仕通
	
	@SerializedName("TransTime")
	private String transTime; // 交易日期yyyyMMddHHmmss
	
	@SerializedName("OpenId")
	private Integer openId; // 第三方渠道唯一id
	
	@SerializedName("CreateTime")
	private String sycTime; // 创建时间
	
	public String getPayId() {
		return payId;
	}
	
	public void setPayId(String payId) {
		this.payId = payId;
	}
	
	public String getUserId() {
		return userId;
	}
	
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	public String getPayFlowNo() {
		return payFlowNo;
	}
	
	public void setPayFlowNo(String payFlowNo) {
		this.payFlowNo = payFlowNo;
	}
	
	public Integer getTransMoney() {
		return transMoney;
	}
	
	public void setTransMoney(Integer transMoney) {
		this.transMoney = transMoney;
	}
	
	public Integer getPayWay() {
		return payWay;
	}
	
	public void setPayWay(Integer payWay) {
		this.payWay = payWay;
	}
	
	public String getTransTime() {
		return transTime;
	}
	
	public void setTransTime(String transTime) {
		this.transTime = transTime;
	}
	
	public Integer getOpenId() {
		return openId;
	}
	
	public void setOpenId(Integer openId) {
		this.openId = openId;
	}
	
    public String getSycTime() {
    	return sycTime;
    }

	
    public void setSycTime(String sycTime) {
    	this.sycTime = sycTime;
    }
}
