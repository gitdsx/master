package com.jy.vo.system.reconciliation.games;

import com.google.gson.annotations.SerializedName;

import java.math.BigDecimal;

/**
 * Created by ZQY on 2017/3/29.
 */
public class Items {
    @SerializedName(value = "UserID")
    private BigDecimal userId;
    @SerializedName(value = "QcDate")
    private String qcDate;
    @SerializedName(value = "itemid")
    private String itemId;
    @SerializedName(value = "DiamondValueBalance")
    private BigDecimal diamondValueBalance;
    @SerializedName(value = "GoldValueBalance")
    private BigDecimal goldValueBalance;
    @SerializedName(value = "GiamondValuelessBalance")
    private BigDecimal diamondValuelessBalance;
    @SerializedName(value = "GoldValuelessBalance")
    private BigDecimal goldValuelessBalance;
    @SerializedName(value = "Count")
    private BigDecimal counts;

    public BigDecimal getUserId() {
        return userId;
    }

    public void setUserId(BigDecimal userId) {
        this.userId = userId;
    }

    public String getQcDate() {
        return qcDate;
    }

    public void setQcDate(String qcDate) {
        this.qcDate = qcDate;
    }

    public String getItemId() {
        return itemId;
    }

    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    public BigDecimal getDiamondValueBalance() {
        return diamondValueBalance;
    }

    public void setDiamondValueBalance(BigDecimal diamondValueBalance) {
        this.diamondValueBalance = diamondValueBalance;
    }

    public BigDecimal getGoldValueBalance() {
        return goldValueBalance;
    }

    public void setGoldValueBalance(BigDecimal goldValueBalance) {
        this.goldValueBalance = goldValueBalance;
    }

    public BigDecimal getDiamondValuelessBalance() {
        return diamondValuelessBalance;
    }

    public void setDiamondValuelessBalance(BigDecimal diamondValuelessBalance) {
        this.diamondValuelessBalance = diamondValuelessBalance;
    }

    public BigDecimal getGoldValuelessBalance() {
        return goldValuelessBalance;
    }

    public void setGoldValuelessBalance(BigDecimal goldValuelessBalance) {
        this.goldValuelessBalance = goldValuelessBalance;
    }

    public BigDecimal getCounts() {
        return counts;
    }

    public void setCounts(BigDecimal counts) {
        this.counts = counts;
    }
}
