package com.jy.from.system.request;

import com.alibaba.fastjson.annotation.JSONField;

import java.io.Serializable;
import java.util.Date;

public class VoucherInfoForm implements Serializable{

	@JSONField(name = "batchNo")
	private String batchNo; // 批次
	@JSONField(name = "schemeId")
	private Integer schemeID;
	@JSONField(name = "usedDate")
	private Date usedDate;//使用时间
	@JSONField(name = "usedMachine")
	private String usedMachine;// 终端编号
	@JSONField(name = "userId")
	private Integer userID;
	@JSONField(name = "vEndDate")
	private String vEnd;//结束时间
	@JSONField(name = "vStartDate")
	private String vStart;//开始时间
	@JSONField(name = "vMoney")
	private String vMoney;// 券金额
	@JSONField(name = "vStatus")
	private String vStatus;// 使用状态
	@JSONField(name = "codeId")
	private String vNo;// 代金券编号
	@JSONField(name = "vhandleStyle")
	private String buyWay;//购买方式

	public String getBatchNo() {
		return batchNo;
	}

	public void setBatchNo(String batchNo) {
		this.batchNo = batchNo;
	}

	public Integer getSchemeID() {
		return schemeID;
	}

	public void setSchemeID(Integer schemeID) {
		this.schemeID = schemeID;
	}

	public Integer getUserID() {
		return userID;
	}

	public void setUserID(Integer userID) {
		this.userID = userID;
	}

	public Date getUsedDate() {
		return usedDate;
	}

	public void setUsedDate(Date usedDate) {
		this.usedDate = usedDate;
	}

	public String getUsedMachine() {
		return usedMachine;
	}

	public void setUsedMachine(String usedMachine) {
		this.usedMachine = usedMachine;
	}

	public String getvEnd() {
		return vEnd;
	}

	public void setvEnd(String vEnd) {
		this.vEnd = vEnd;
	}

	public String getvStart() {
		return vStart;
	}

	public void setvStart(String vStart) {
		this.vStart = vStart;
	}

	public String getvMoney() {
		return vMoney;
	}

	public void setvMoney(String vMoney) {
		this.vMoney = vMoney;
	}

	public String getvStatus() {
		return vStatus;
	}

	public void setvStatus(String vStatus) {
		this.vStatus = vStatus;
	}

	public String getvNo() {
		return vNo;
	}

	public void setvNo(String vNo) {
		this.vNo = vNo;
	}

	public String getBuyWay() {
		return buyWay;
	}

	public void setBuyWay(String buyWay) {
		this.buyWay = buyWay;
	}
}
