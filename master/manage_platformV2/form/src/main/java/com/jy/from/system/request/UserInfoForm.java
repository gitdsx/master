package com.jy.from.system.request;

import com.alibaba.fastjson.annotation.JSONField;

import java.io.Serializable;

/**
 * Created by lijunke on 2017/4/24.
 */
public class UserInfoForm  implements Serializable {

    @JSONField(name = "userId")
    private Integer userId;
    @JSONField(name = "userName")
    private String userName;
    @JSONField(name="name")
    private String name;
    @JSONField(name="userPhone")
    private String phone;
    @JSONField(name = "isSeller")
    private int isSeller;
    @JSONField(name = "belongTo")
    private Integer marketer;
    @JSONField(name = "userType")
    private int userType;
    @JSONField(name = "isEnable")
    private int isUse; //isEnable
    @JSONField(name = "belongDate")
    private String marketTime;   //belongDate
    @JSONField(name = "isactivity")
    private int isactivity;
    private Double money;
    private String createTime;

    public Integer getMarketer() {
        return marketer;
    }

    public void setMarketer(Integer marketer) {
        this.marketer = marketer;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public Double getMoney() {
        return money;
    }

    public void setMoney(Double money) {
        this.money = money;
    }

    public int getIsactivity() {
        return isactivity;
    }

    public void setIsactivity(int isactivity) {
        this.isactivity = isactivity;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public int getUserType() {
        return userType;
    }

    public void setUserType(int userType) {
        this.userType = userType;
    }

    public int getIsSeller() {
        return isSeller;
    }

    public void setIsSeller(int isSeller) {
        this.isSeller = isSeller;
    }

    public int getIsUse() {
        return isUse;
    }

    public void setIsUse(int isUse) {
        this.isUse = isUse;
    }

    public String getMarketTime() {
        return marketTime;
    }

    public void setMarketTime(String marketTime) {
        this.marketTime = marketTime;
    }
}
