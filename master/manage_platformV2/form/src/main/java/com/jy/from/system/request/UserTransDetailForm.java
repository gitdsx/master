package com.jy.from.system.request;

import com.alibaba.fastjson.annotation.JSONField;

import java.io.Serializable;

/**
 * Created by shixi on 2017/4/27.
 */
public class UserTransDetailForm implements Serializable
{
    private String sysDate;
    @JSONField(name ="userId")
    private String userId;
    @JSONField(name="transMoney")
    private Double money;
    @JSONField(name ="transStatus")
    private String tranType;
    @JSONField(name="transChannel")
    private String tranChannel;
    @JSONField(name ="transTime")
    private String tranTime;
    @JSONField(name ="payFlowNo")
    private String payFlowNo;
    @JSONField(name ="payid")
    private String payId;
    @JSONField(name ="batchNo")
    private String batchNo;

    public String getSysDate()
    {
        return sysDate;
    }

    public void setSysDate(String sysDate)
    {
        this.sysDate = sysDate;
    }

    public String getUserId()
    {
        return userId;
    }

    public void setUserId(String userId)
    {
        this.userId = userId;
    }

    public Double getMoney()
    {
        return money;
    }

    public void setMoney(Double money)
    {
        this.money = money;
    }

    public String getTranType()
    {
        return tranType;
    }

    public void setTranType(String tranType)
    {
        this.tranType = tranType;
    }

    public String getTranChannel()
    {
        return tranChannel;
    }

    public void setTranChannel(String tranChannel)
    {
        this.tranChannel = tranChannel;
    }

    public String getTranTime()
    {
        return tranTime;
    }

    public void setTranTime(String tranTime)
    {
        this.tranTime = tranTime;
    }

    public String getPayFlowNo()
    {
        return payFlowNo;
    }

    public void setPayFlowNo(String payFlowNo)
    {
        this.payFlowNo = payFlowNo;
    }

    public String getPayId()
    {
        return payId;
    }

    public void setPayId(String payId)
    {
        this.payId = payId;
    }

    public String getBatchNo()
    {
        return batchNo;
    }

    public void setBatchNo(String batchNo)
    {
        this.batchNo = batchNo;
    }
}
